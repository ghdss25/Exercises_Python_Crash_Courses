## 5-11. Ordinal Numbers: Ordinal numbers indicate their position in a list, such
## as 1st or 2nd. Most ordinal numbers end in th, except 1, 2, and 3.

## Store the numbers 1 through 9 in a list.
## Loop through the list.
## Use an if-elif-else chain inside the loop to print the proper
## ordinal ending for each number. Your output should read "1st 2nd 3rd 4th 5th 6th
## 7th 8th 9th", and each result should be on a separate line.

numbers = ['1th', '2th', '3th', '4th', '5th', '6th', '7th', '8th', '9th']

for n in numbers:

      if n == '1th':

            print(numbers[0])

      elif n == '2th':

            print(numbers[1])

      elif n == '3th':

            print(numbers[2])

      elif n == '4th':

            print(numbers[3])

      elif n == '5th':

            print(numbers[4])

      elif n == '6th':

            print(numbers[5])

      elif n == '7th':

            print(numbers[6])

      elif n == '8th':

            print(numbers[7])

      elif n == '9th':

            print(numbers[8])

      
